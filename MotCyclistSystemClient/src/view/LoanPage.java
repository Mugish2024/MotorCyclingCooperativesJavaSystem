        private void initializeServices() {
        try {
            Registry registry = LocateRegistry.getRegistry("127.0.0.1", 2325);
            savingsService = (SavingsAccountService) registry.lookup("SavingsAccountService");
            loanService = (LoanService) registry.lookup("LoanService");
            if (savingsService == null || loanService == null) {
                throw new Exception("Failed to lookup services");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Cannot connect to server: " + e.getMessage(), "Connection Error", JOptionPane.ERROR_MESSAGE);
            System.exit(1);
        }
    }

    private void initializeTable() {
        tableModel = new DefaultTableModel(new Object[]{"Name", "Amount", "Due Date", "Interest Rate", "Status"}, 0);
        jTable1.setModel(tableModel);
    }

    private void loadLoans() {
        try {
            tableModel.setRowCount(0);
            List<Loan> loans = loanService.findAll();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            for (Loan loan : loans) {
                String name = loan.getMotorcyclist() != null ? loan.getMotorcyclist().getName() : "Unknown";
                tableModel.addRow(new Object[]{
                    name,
                    String.format("ZMW %.2f", loan.getAmount()),
                    sdf.format(loan.getDueDate()),
                    String.format("%.2f%%", loan.getInterestRate()),
                    loan.isIsRepaid() ? "Paid" : "Unpaid"
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading loans: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }



