    private void RequestLoanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RequestLoanButtonActionPerformed
        // TODO add your handling code here:
         try {
            String accountNumber = AccNbrTxt.getText().trim();
            String loanAmountStr = LoanAmountTxt.getText().trim();

            if (accountNumber.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Account number is required.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (loanAmountStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Loan amount is required.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            double loanAmount;
            try {
                loanAmount = Double.parseDouble(loanAmountStr);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Loan amount must be a valid number.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (loanAmount <= 0) {
                JOptionPane.showMessageDialog(this, "Loan amount must be positive.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            SavingsAccount account = null;
            List<SavingsAccount> accounts = savingsService.findAll();
            for (SavingsAccount acc : accounts) {
                if (accountNumber.equals(acc.getAccountNumber())) {
                    account = acc;
                    break;
                }
            }
            if (account == null) {
                JOptionPane.showMessageDialog(this, "Account not found.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (loanAmount > account.getBalance()) {
                JOptionPane.showMessageDialog(this, "Loan amount cannot exceed current balance.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Motorcyclist motorcyclist = account.getMotorcyclist();
            if (motorcyclist == null) {
                JOptionPane.showMessageDialog(this, "Motorcyclist not associated with account.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            List<Loan> loans = loanService.findByMotorcyclistId(motorcyclist.getId());
            for (Loan loan : loans) {
                if (!loan.isIsRepaid()) {
                    JOptionPane.showMessageDialog(this, "Cannot request loan: Unpaid loan exists.", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

            Loan loan = new Loan();
            loan.setAmount(loanAmount);
            loan.setInterestRate(5.0); // Fixed 5%
            loan.setIssueDate(new Date());
            Calendar cal = Calendar.getInstance();
            cal.add(Calendar.YEAR, 1);
            loan.setDueDate(cal.getTime());
            loan.setIsRepaid(false);
            loan.setMotorcyclist(motorcyclist);
            loanService.save(loan);

            account.setBalance(account.getBalance() + loanAmount);
            savingsService.update(account);

            JOptionPane.showMessageDialog(this, "Loan of ZMW " + loanAmount + " granted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadLoans();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error requesting loan: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_RequestLoanButtonActionPerformed

    private void CheckLoanstutsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckLoanstutsButtonActionPerformed
        // TODO add your handling code here:
        try {
            String accountNumber = jTextField3.getText().trim();
            if (accountNumber.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Account number is required.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            SavingsAccount account = null;
            List<SavingsAccount> accounts = savingsService.findAll();
            for (SavingsAccount acc : accounts) {
                if (accountNumber.equals(acc.getAccountNumber())) {
                    account = acc;
                    break;
                }
            }
            if (account == null) {
                JOptionPane.showMessageDialog(this, "Account not found.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Motorcyclist motorcyclist = account.getMotorcyclist();
            if (motorcyclist == null) {
                JOptionPane.showMessageDialog(this, "Motorcyclist not associated with account.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            List<Loan> loans = loanService.findByMotorcyclistId(motorcyclist.getId());
            if (loans.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No loans found for this account.", "Info", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            StringBuilder status = new StringBuilder("Loan Status for Account: " + accountNumber + "\n\n");
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            for (Loan loan : loans) {
                status.append("Loan ID: ").append(loan.getLoanId()).append("\n")
                      .append("Amount: ZMW ").append(String.format("%.2f", loan.getAmount())).append("\n")
                      .append("Issue Date: ").append(sdf.format(loan.getIssueDate())).append("\n")
                      .append("Due Date: ").append(sdf.format(loan.getDueDate())).append("\n")
                      .append("Interest Rate: ").append(String.format("%.2f%%", loan.getInterestRate())).append("\n")
                      .append("Status: ").append(loan.isIsRepaid() ? "Paid" : "Unpaid").append("\n\n");
            }

            JOptionPane.showMessageDialog(this, status.toString(), "Loan Status", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error checking loan status: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_CheckLoanstutsButtonActionPerformed

