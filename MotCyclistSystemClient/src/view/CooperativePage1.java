    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        try {
        if (IDTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Select a cooperative to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this cooperative?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(IDTxt.getText().trim());
            Cooperatives cooperative = service.findById(id);
            if (cooperative == null) {
                JOptionPane.showMessageDialog(this, "Cooperative not found.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            service.delete(cooperative);
            JOptionPane.showMessageDialog(this, "Cooperative deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            IDTxt.setText("");
            NameTxt.setText("");
            LocationTxt.setText("");
            InterestRateTxt.setText("");
            SearchIdTxt.setText("");
            loadCooperatives();
        }
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Invalid ID: Must be a number.", "Error", JOptionPane.ERROR_MESSAGE);
        System.err.println("Invalid input: " + ex.getMessage());
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error deleting cooperative: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        System.err.println("Delete failed: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_DeleteButtonActionPerformed

    private void SearchByIdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchByIdButtonActionPerformed
        // TODO add your handling code here:
        try {
        String searchId = SearchIdTxt.getText().trim();
        if (searchId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter an ID to search.", "Error", JOptionPane.ERROR_MESSAGE);
            loadCooperatives();
            return;
        }
        int id = Integer.parseInt(searchId);
        Cooperatives cooperative = service.findById(id);
        tableModel.setRowCount(0);
        if (cooperative != null) {
            tableModel.addRow(new Object[]{
                cooperative.getId(),
                cooperative.getName() != null ? cooperative.getName() : "",
                cooperative.getLocation() != null ? cooperative.getLocation() : "",
                cooperative.getInterestRate()
            });
        } else {
            JOptionPane.showMessageDialog(this, "No cooperative found with ID " + id, "Info", JOptionPane.INFORMATION_MESSAGE);
        }
        SearchIdTxt.setText("");
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Invalid ID: Must be a number.", "Error", JOptionPane.ERROR_MESSAGE);
        System.err.println("Invalid input: " + ex.getMessage());
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error searching cooperative: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        System.err.println("Search failed: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_SearchByIdButtonActionPerformed

