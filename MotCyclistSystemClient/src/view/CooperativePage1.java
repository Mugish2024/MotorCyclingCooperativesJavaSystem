   private void initializeService() {
    try {
        Registry registry = LocateRegistry.getRegistry("127.0.0.1", 2325);
        service = (CooperativesService) registry.lookup("CooperativesService");
        if (service == null) {
            throw new Exception("Failed to lookup CooperativesService");
        }
        System.out.println("Connected to CooperativesService at 127.0.0.1:2325");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Cannot connect to server: " + e.getMessage(), "Connection Error", JOptionPane.ERROR_MESSAGE);
        System.err.println("Connection failed: " + e.getMessage());
        e.printStackTrace();
        System.exit(1); // Ensure exit
    }
}
 private void initializeTable() {
    tableModel = new DefaultTableModel(new String[]{"ID", "Name", "Location", "Interest Rate"}, 0); // Use class field
    jTable1.setModel(tableModel);
    jTable1.getSelectionModel().addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting() && jTable1.getSelectedRow() != -1) {
            int row = jTable1.getSelectedRow();
            IDTxt.setText(tableModel.getValueAt(row, 0).toString());
            NameTxt.setText(tableModel.getValueAt(row, 1).toString());
            LocationTxt.setText(tableModel.getValueAt(row, 2).toString());
            InterestRateTxt.setText(tableModel.getValueAt(row, 3).toString());
        }
    });
}
private void loadCooperatives() {
    try {
        if (service == null) {
            JOptionPane.showMessageDialog(this, "Service not initialized. Please restart the application.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        tableModel.setRowCount(0); // Clear table
        List<Cooperatives> cooperatives = service.findAll();
        if (cooperatives != null) {
            for (Cooperatives c : cooperatives) {
                if (c != null) { // Avoid null cooperatives
                    tableModel.addRow(new Object[]{
                        c.getId(),
                        c.getName() != null ? c.getName() : "",
                        c.getLocation() != null ? c.getLocation() : "",
                        c.getInterestRate()
                    });
                }
            }
        } else {
            System.out.println("No cooperatives found or service returned null.");
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error loading cooperatives: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        System.err.println("Load failed: " + ex.getMessage());
        ex.printStackTrace();
    }
}

