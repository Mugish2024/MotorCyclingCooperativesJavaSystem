    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        // TODO add your handling code here:
        try {
        if (NameTxt.getText().trim().isEmpty() || LocationTxt.getText().trim().isEmpty() || InterestRateTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        double interestRate = Double.parseDouble(InterestRateTxt.getText().trim());
        if (interestRate < 0 || interestRate > 100) {
            JOptionPane.showMessageDialog(this, "Interest rate must be between 0 and 100.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Cooperatives cooperative = new Cooperatives();
        cooperative.setName(NameTxt.getText().trim());
        cooperative.setLocation(LocationTxt.getText().trim());
        cooperative.setInterestRate(interestRate);
        service.save(cooperative);
        JOptionPane.showMessageDialog(this, "Cooperative saved successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        IDTxt.setText("");
        NameTxt.setText("");
        LocationTxt.setText("");
        InterestRateTxt.setText("");
        SearchIdTxt.setText("");
        loadCooperatives();
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Invalid interest rate: Must be a number.", "Error", JOptionPane.ERROR_MESSAGE);
        System.err.println("Invalid input: " + ex.getMessage());
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        System.err.println("Save failed: " + ex.getMessage());
    }
    }//GEN-LAST:event_SaveButtonActionPerformed

