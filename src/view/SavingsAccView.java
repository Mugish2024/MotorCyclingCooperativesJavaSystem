     void loadAccountData() {
    SavingAccountDao accountDao = new SavingAccountDao();
    List<SavingsAccount> accounts = accountDao.getAllAccounts();
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Clear existing data

    MotorcyclistDao riderDao = new MotorcyclistDao(); // Create DAO instance once
    
    for (SavingsAccount account : accounts) {
        String riderName = riderDao.getRiderName(account.getRiderId());
        
        model.addRow(new Object[]{
            account.getAccountId(),
            account.getAccountNumber(),
            account.getRiderId(),
            account.getBalance(),
            account.getLastInterestDate() != null ? 
                dateFormat.format(account.getLastInterestDate()) : "N/A",
            riderName // Use the actual rider name
        });
    }
}
  private String getRiderName(int riderId) {
    MotorcyclistDao riderDao = new MotorcyclistDao();
    return riderDao.getRiderName(riderId);
}
