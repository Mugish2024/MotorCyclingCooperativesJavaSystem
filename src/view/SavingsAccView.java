    private void depositButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_depositButtonActionPerformed
        // TODO add your handling code here:
         int selectedRow = jTable1.getSelectedRow();
    if (selectedRow >= 0) {
        // Get account info from selected row
        String accountNumber = jTable1.getValueAt(selectedRow, 1).toString();
        String riderName = jTable1.getValueAt(selectedRow, 5).toString();
        
        // Create and show deposit form
        Depositform depositForm = new Depositform(this);
        depositForm.setSelectedAccount(accountNumber, riderName);
        
        JDialog dialog = new JDialog(this, "Deposit to Account", true);
        dialog.getContentPane().add(depositForm.getContentPane());
        dialog.pack();
        dialog.setLocationRelativeTo(this);
        dialog.setVisible(true);
    } else {
        JOptionPane.showMessageDialog(this, 
            "Please select an account first", 
            "No Selection", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_depositButtonActionPerformed

